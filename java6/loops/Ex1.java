package loops;

public class Ex1 {

	public static void main(String[] args) {
		
		//
		/*
		 use loops: execute same code multiple times but on different data.
		 
		 loops : for , while , do while
		 
		 */
		
		//print hello for 10 times
		
		for( int i=1 ; i <=10; i++) {
			
			System.out.println("hello");
		}
		
		//  int i=1 ;  -> initialization   ( 1 time)
		//  i <=10;    -> condition        ( every time  before the for logic)
		//  i++        -> increment/decrement (every time after for logic)
	}
	
	
}
