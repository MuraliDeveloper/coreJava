package misc;

public class MathEx {
	 public static void main(String args[])
	  {
	    System.out.println("This program illustrates Math class functions!");
	    System.out.println();
	    System.out.println("Absolute function : ");
	    System.out.println("abs(-5) = " + Math.abs(-5) );
	    System.out.println("abs(5) = " + Math.abs(5) );
	    System.out.println();
	    System.out.println("ceil and floor function : ");
	    System.out.println("ceil(8.2) = " + Math.ceil(8.2) );
	    System.out.println("floor(8.2) = " + Math.floor(8.2) );
	    System.out.println();
	    System.out.println("pow and sqrt : ");
	    System.out.println("pow(5,(raised) 3) = " + Math.pow(5,3) );
	    System.out.println("sqrt(9) = " + Math.sqrt(9) );
	   
	    System.out.println();
	    System.out.println("min max function : ");
	    System.out.println("min(8.3,7.5) = " + Math.min(8.3,7.5) );
	    System.out.println("max(8.3,7.5) = " + Math.max(8.3,7.5) );
	    //pg 418 = complete reference java for complete math class
	  }
}
