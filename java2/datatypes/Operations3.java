package datatypes;

public class Operations3 {

	public static void main(String[] args) {
		int x= 90;
		
		int z= x;  // assign the value of x to z
		System.out.println(z);
		
		//meaning of ++x  = x = x+1 ;
		int y = ++x; //increment the value of x and  assign the value of x to y pre increment
		System.out.println(y);
		
		System.out.println(x);
	}
	
}
