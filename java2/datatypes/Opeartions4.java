package datatypes;

public class Opeartions4 {

	public static void main(String[] args) {
		int n1= 100;
		int n2 =80;
		
		//perform sum , sub , mul , div , remainder
		
		int sum = n1 + n2;
		int sub = n1 - n2;
		int mul = n1 * n2;
		int div = n1/n2;
		int remainder = n1%n2;
		
		
		//concatenation or join ; +is used for join operations
		System.out.println("sum value is " + sum);
		System.out.println("substraction result = "+sub);
		System.out.println("mul result  = "+mul);
		System.out.println("div result is "+div);
		System.out.println("remainder is "+remainder);
		
		//output as : sum = 180 , sub = 20 .......
	}
}
